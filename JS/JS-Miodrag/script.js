var students = [
  ["David", 80],
  ["Marko", 77],
  ["Dany", 88],
  ["John", 95],
  ["Thomas", 68],
];
var average = 0;
var grade = 0;
var sum = 0;
for (var i = 0; i < students.length; i++) {
  for (var j = 0; j < students[i].length; j++) {
    if (j % 2 != 0) {
      sum = sum + students[i][j];
    }
  }
}

average = sum / students.length;
console.log(average);
if (average < 60) {
  console.log("F");
} else if (average < 70) {
  console.log("D");
} else if (average < 80) {
  console.log("C");
} else if (average < 90) {
  console.log("B");
} else console.log("A");
